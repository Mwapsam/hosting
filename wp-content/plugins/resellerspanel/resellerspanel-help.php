<h2>Resellers-Panel Help</h2>
<h3>Setup</h3>
<p>Go to the connection page and select you Resellers Panel username and password and hit "Save changes".
If the username or password is invalid, error message will be displayed.</p>

<h3>Usage</h3>
<p>Use <strong>[rp_plans_short type="shared" best="1"]</strong> short tag in a page to display a table of information about the Shared hosting plans.</p>
<p>Use <strong>[rp_plans type="vps_kvm" best="1"]</strong> short tag in a page to display a table of information about the VPS Virtuozzo plans.</p>
<p>Use <strong>[rp_plans type="vps_openvz" best="1"]</strong> short tag in a page to display a table of information about the shared VPS OpenVZ plans.</p>
<p>Use <strong>[rp_plans type="semi_dedicated" best="1"]</strong> short tag in a page to display a table of information about the Semi-Dedicated hosting plans.</p>
<p>Use <strong>[rp_plans type="dedicated" best="1"]</strong> short tag in a page to display a table of information about the Dedicated plans.</p>
<p>Use <strong>[domain_search_box variant="7"]</strong> short tag in a page to display a domain search form</p>
<p>Use <strong>[rp_tld_price]</strong> short tag in a page to display a TLD pricing table</p>
<p>Use <strong>[rp_tld_info]</strong> short tag in a page to display a TLD information table</p>
<p>Use <strong>[rp_support_phone] </strong> short tag in a page to display the selected support phone from connections in templates</p>
<p>Use <strong>[rp_support_id]</strong> short tag in a page to display the store id in templates</p>
